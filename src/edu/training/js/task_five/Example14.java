package edu.training.js.task_five;

public class Example14 {

	public static void main(String[] args) {

        int n = 3;
        double sum = 0;

        for (double i = 1; i <= n; i++) {
            sum += 1 / i;
        }

        System.out.println("n = " + n + " sum = " + sum);

	}

}
