package edu.training.js.task_five;

public class Example1 {

	public static void main(String[] args) {
		
		int x = 1;
		int y = 5;
		
		while (x <= 5) {
			System.out.println(x);
			x++;			
		}
		System.out.println();
		while (y >= 1) {
			System.out.println(y);
			y--;			
		}

	}

}
